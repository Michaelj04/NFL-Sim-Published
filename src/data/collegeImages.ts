function hash(value: string): number {
  let result = 2166136261;
  for (let index = 0; index < value.length; index += 1) {
    result ^= value.charCodeAt(index);
    result = Math.imul(result, 16777619);
  }
  return result >>> 0;
}

function initialsFor(name: string): string {
  const words = name
    .replace(/\b(The|of|and|at)\b/gi, "")
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean);
  if (words.length === 1) return words[0].slice(0, 3).toUpperCase();
  return words.slice(0, 3).map((word) => word[0]).join("").toUpperCase();
}

function espnLogoUrl(id: number): string {
  return `https://a.espncdn.com/i/teamlogos/ncaa/500/${id}.png`;
}

export function collegeImageFor(program: Pick<CollegeProgram, "id" | "name" | "conference">): CollegeImageMeta {
  const colorIndex = hash(`${program.id}:${program.conference}`) % palettes.length;
  const [primaryColor, secondaryColor] = palettes[colorIndex];
  const exactEspnId = espnLogoIds[program.id];
  const schoolEspnId = schoolNameEspnLogoIds[program.name];
  const localLogoUrl = localLogoUrls[program.id];
  const preferredLogoUrls = preferredLogoUrlChains[program.id];
  const logoUrls = [...new Set([
    ...(preferredLogoUrls ?? []),
    exactEspnId ? espnLogoUrl(exactEspnId) : undefined,
    !exactEspnId && schoolEspnId ? espnLogoUrl(schoolEspnId) : undefined,
    localLogoUrl
  ].filter((candidate): candidate is string => Boolean(candidate)))];
  const primaryLogoUrl = logoUrls[0];
  return {
    logoUrl: primaryLogoUrl,
    logoUrls: logoUrls.length ? logoUrls : undefined,
    logoSource: exactEspnId || schoolEspnId ? "espn" : localLogoUrl ? "local" : "generated",
    logoInitials: initialsFor(program.name),
    primaryColor,
    secondaryColor
  };
}

export function withCollegeImage<T extends Pick<CollegeProgram, "id" | "name" | "conference">>(program: T): T & CollegeImageMeta {
  return {
    ...program,
    ...collegeImageFor(program)
  };
}
